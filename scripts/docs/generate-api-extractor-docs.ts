import { mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";

type ApiSourceEntry = {
  docsRelativePath: string;
  exportKey: string;
  packageDir: string;
  packageName: string;
  repoRelativePath: string;
};

type PackageExportEntry = {
  namespace: null | string;
  source: ApiSourceEntry;
};

const repoRoot = path.resolve(import.meta.dir, "../..");
const inventoryPath = path.join(repoRoot, "apps/docs/api-sources.json");
const outputRoot = path.join(repoRoot, "apps/docs-hono/content/docs");
const checkMode = process.argv.includes("--check");

const packageBuildOrder = [
  "packages/core",
  "packages/template",
  "packages/provider",
  "packages/messaging",
  "packages/analytics",
  "packages/channel",
  "packages/webhook",
  "packages/k-msg",
] as const;

function packageSlug(packageName: string): string {
  return packageName.replace(/^@k-msg\//, "");
}

function toPosix(value: string): string {
  return value.replaceAll(path.sep, "/");
}

function exportNamespace(exportKey: string): null | string {
  if (exportKey === ".") {
    return null;
  }

  const trimmed = exportKey.replace(/^\.\//, "");
  return trimmed.replaceAll("/", "_").replaceAll("-", "_");
}

function declarationPathFromSource(source: ApiSourceEntry): string {
  const packageRoot = path.join(repoRoot, source.packageDir);
  const sourcePath = path.join(repoRoot, source.repoRelativePath);
  const relativeToPackage = path.relative(packageRoot, sourcePath);
  return relativeToPackage.replace(/^src\//, "dist/").replace(/\.ts$/, ".d.ts");
}

function routePrefix(locale: "en" | "ko"): string {
  return locale === "en" ? "/en" : "";
}

function stripGeneratedHeader(markdown: string): string {
  return markdown
    .replace(/^<!-- Do not edit this file\.[\s\S]*?-->\r?\n\r?\n/, "")
    .trimStart();
}

function frontmatterBlock(params: {
  description?: string;
  sourcePath: string;
  title?: string;
}): string {
  const lines = ["---"];
  if (params.title) {
    lines.push(`title: ${JSON.stringify(params.title)}`);
  }
  if (params.description) {
    lines.push(`description: ${JSON.stringify(params.description)}`);
  }
  lines.push(`sourcePath: ${JSON.stringify(params.sourcePath)}`);
  lines.push("---", "");
  return lines.join("\n");
}

function headingFromMarkdown(markdown: string): string | null {
  const match = markdown.match(/^##\s+(.+)$/m);
  return match?.[1]?.trim() ?? null;
}

function apiIndexMarkdown(locale: "en" | "ko", packageNames: string[]): string {
  const title = locale === "ko" ? "API 문서" : "API Reference";
  const description =
    locale === "ko"
      ? "API Extractor 기반의 API 문서입니다."
      : "API reference generated with API Extractor.";
  const links = packageNames
    .map((name) => {
      const slug = packageSlug(name);
      return `- [${name}](${routePrefix(locale)}/api/${slug}/)`;
    })
    .join("\n");

  return `${frontmatterBlock({
    description,
    sourcePath: "apps/docs/api-sources.json",
    title,
  })}${links}\n`;
}

async function readInventory(): Promise<ApiSourceEntry[]> {
  const raw = await readFile(inventoryPath, "utf8");
  return JSON.parse(raw) as ApiSourceEntry[];
}

async function runCommand(args: string[], cwd: string): Promise<void> {
  const proc = Bun.spawn(args, {
    cwd,
    stderr: "inherit",
    stdout: "inherit",
  });
  const exitCode = await proc.exited;
  if (exitCode !== 0) {
    throw new Error(
      `Command failed (${exitCode}): ${args.join(" ")} (cwd: ${cwd})`,
    );
  }
}

async function buildTypeDeclarations(): Promise<void> {
  for (const packageDir of packageBuildOrder) {
    await runCommand(
      ["bun", "run", "--cwd", packageDir, "build:types"],
      repoRoot,
    );
  }
}

function buildSyntheticEntry(
  entries: PackageExportEntry[],
  workDir: string,
): string {
  const lines: string[] = [];

  for (const entry of entries) {
    const packageRoot = path.join(repoRoot, entry.source.packageDir);
    const declarationPath = path.join(
      packageRoot,
      declarationPathFromSource(entry.source),
    );
    let modulePath = toPosix(path.relative(workDir, declarationPath)).replace(
      /\.d\.ts$/,
      "",
    );
    if (!modulePath.startsWith(".")) {
      modulePath = `./${modulePath}`;
    }

    if (entry.namespace === null) {
      lines.push(`export * from "${modulePath}";`);
    } else {
      lines.push(`export * as ${entry.namespace} from "${modulePath}";`);
    }
  }

  return `${lines.join("\n")}\n`;
}

